package com.prarthana.student.stud;

/**
 *
 * @author Pavilion G4
 */
public class Student {

    private int id, roll;
    private String name, faculty, address;
    private boolean status;

    public Student() {
    }

    public Student(int id, int roll, String name, String faculty, String address, boolean status) {
        this.id = id;
        this.roll = roll;
        this.name = name;
        this.faculty = faculty;
        this.address = address;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getRoll() {
        return roll;
    }

    public void setRoll(int roll) {
        this.roll = roll;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFaculty() {
        return faculty;
    }

    public void setFaculty(String faculty) {
        this.faculty = faculty;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }

}
